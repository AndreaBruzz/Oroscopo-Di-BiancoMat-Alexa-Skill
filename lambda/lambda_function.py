class OroscopoIntentHandler(AbstractRequestHandler):
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        return ask_utils.is_intent_name("OroscopoIntent")(handler_input)
        
    def handle(self, handler_input):
        slots = handler_input.request_envelope.request.intent.slots
        URL = "https://www.biancomat.it/oroscopo-di-oggi-"
        sign = slots['zodiacSign'].value
        sign = sign.lower()
        URL += sign + "/"
        page = requests.get(URL)
        soup = BeautifulSoup(page.content, "html.parser")
        selected_id = "eael-post-grid-"
        if sign == "ariete":
            selected_id += "8d497f7"
        else:
            selected_id += "f6763be"
        results = soup.find(id = selected_id)
        content_elems = results.find_all("div", class_="eael-grid-post-excerpt")
        speak_output = content_elems[0].text
        speak_output = speak_output.replace("<3", ", !cuoricino!")
        return (
            handler_input.response_builder
                .speak(speak_output)
                .response
        )

